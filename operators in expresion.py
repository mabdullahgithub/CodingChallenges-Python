#operators in an expression
# +, -, *, /, //, %, **, ()

print(2+3) #addition
print(2-3) #subtraction
print(2*3) #multiplication
print(2/3) #division
print(10//3) #floor division (rounds down) which means it will estimate the number of times 3 goes into 10
print(11%3) #modulus (remainder)
print(2**3) #exponent 2^3

print(2+3*4) #order of operations
print((2+3)*4) #order of operations
print(2**3**2) #order of operations 2^3^2 or 2^(3^2) or 2^9

print(2+3*4/2-2) #order of operations